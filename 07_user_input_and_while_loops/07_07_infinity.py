#Initialize the variable 'number' with a starting value of 501.
number = 501

#Start a loop that will continue as long as 'number' is less than or equal to 1000.
while number <= 1000:
    #Print the current value of 'number'.
    print(number)
    
    #Increment 'number' by 1 to avoid an infinite loop.
    number += 1
games = ['Zelda', 'Halo', 'Horizon', 'Minecraft', 'Pokemon']
#This prints the first three items in the list.
message = f"The first three items in the list are: {games[0:3]}."
print (message)

#This prints the middle item of the list.
message_2 = f"The item from the middle of the list is: {games[2]}"
print(message_2)

#This prints the last three items of the list.
message_3 = f"The last three items in the list are: {games[-3:]}"
print (message_3)
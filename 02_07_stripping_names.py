name = "\t\t\tElijah Kurland\n\n\n"
print (name)

print ("\n\n\nUsing lstrip():")
print (name.lstrip())

print ("\n\n\nUsing rstrip():")
print (name.rstrip())

print ("\n\n\nUsing strip():")
print (name.strip())

